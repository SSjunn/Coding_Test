import java.util.*;
/*
정수 -> nextInt()로 값 받으면 됌
*/
public class Solution {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int a = sc.nextInt();
        int b = sc.nextInt();
        
        System.out.print(a + " + " + b + " = " + (a+b));
        
    }
}