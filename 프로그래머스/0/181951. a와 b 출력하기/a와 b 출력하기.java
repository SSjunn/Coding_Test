import java.util.Scanner;
/*
제한 사항 -> int 형 범위
지문에서 -> 정수라고 지목
nextInt() 사용
*/

public class Solution {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int a = sc.nextInt();
        int b = sc.nextInt();
        
        System.out.println("a = " + a );
        System.out.println("b = " + b );
    }
}
